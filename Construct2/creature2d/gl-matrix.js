mat3_.mul = mat3_.multiply;

/**
 * Translate a mat3_ by the given vector
 *
 * @param {mat3_} out the receiving matrix
 * @param {mat3_} a the matrix to translate
 * @param {vec2_} v vector to translate by
 * @returns {mat3_} out
 */
mat3_.translate = function(out, a, v) {
    var a00 = a[0], a01 = a[1], a02 = a[2],
        a10 = a[3], a11 = a[4], a12 = a[5],
        a20 = a[6], a21 = a[7], a22 = a[8],
        x = v[0], y = v[1];

    out[0] = a00;
    out[1] = a01;
    out[2] = a02;

    out[3] = a10;
    out[4] = a11;
    out[5] = a12;

    out[6] = x * a00 + y * a10 + a20;
    out[7] = x * a01 + y * a11 + a21;
    out[8] = x * a02 + y * a12 + a22;
    return out;
};

/**
 * Rotates a mat3_ by the given angle
 *
 * @param {mat3_} out the receiving matrix
 * @param {mat3_} a the matrix to rotate
 * @param {Number} rad the angle to rotate the matrix by
 * @returns {mat3_} out
 */
mat3_.rotate = function (out, a, rad) {
    var a00 = a[0], a01 = a[1], a02 = a[2],
        a10 = a[3], a11 = a[4], a12 = a[5],
        a20 = a[6], a21 = a[7], a22 = a[8],

        s = Math.sin(rad),
        c = Math.cos(rad);

    out[0] = c * a00 + s * a10;
    out[1] = c * a01 + s * a11;
    out[2] = c * a02 + s * a12;

    out[3] = c * a10 - s * a00;
    out[4] = c * a11 - s * a01;
    out[5] = c * a12 - s * a02;

    out[6] = a20;
    out[7] = a21;
    out[8] = a22;
    return out;
};

/**
 * Scales the mat3_ by the dimensions in the given vec2_
 *
 * @param {mat3_} out the receiving matrix
 * @param {mat3_} a the matrix to rotate
 * @param {vec2_} v the vec2_ to scale the matrix by
 * @returns {mat3_} out
 **/
mat3_.scale = function(out, a, v) {
    var x = v[0], y = v[1];

    out[0] = x * a[0];
    out[1] = x * a[1];
    out[2] = x * a[2];

    out[3] = y * a[3];
    out[4] = y * a[4];
    out[5] = y * a[5];

    out[6] = a[6];
    out[7] = a[7];
    out[8] = a[8];
    return out;
};

