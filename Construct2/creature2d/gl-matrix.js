var quat_ = {};

/**
 * Creates a new identity quat_
 *
 * @returns {quat_} a new quaternion
 */
quat_.create = function() {
    var out = new GLMAT_ARRAY_TYPE(4);
    out[0] = 0;
    out[1] = 0;
    out[2] = 0;
    out[3] = 1;
    return out;
};

/**
 * Sets a quaternion to represent the shortest rotation from one
 * vector to another.
 *
 * Both vectors are assumed to be unit length.
 *
 * @param {quat_} out the receiving quaternion.
 * @param {vec3_} a the initial vector
 * @param {vec3_} b the destination vector
 * @returns {quat_} out
 */
quat_.rotationTo = (function() {
    var tmpvec3 = vec3_.create();
    var xUnitVec3 = vec3_.fromValues(1,0,0);
    var yUnitVec3 = vec3_.fromValues(0,1,0);

    return function(out, a, b) {
        var dot = vec3_.dot(a, b);
        if (dot < -0.999999) {
            vec3_.cross(tmpvec3, xUnitVec3, a);
            if (vec3_.length(tmpvec3) < 0.000001)
                vec3_.cross(tmpvec3, yUnitVec3, a);
            vec3_.normalize(tmpvec3, tmpvec3);
            quat_.setAxisAngle(out, tmpvec3, Math.PI);
            return out;
        } else if (dot > 0.999999) {
            out[0] = 0;
            out[1] = 0;
            out[2] = 0;
            out[3] = 1;
            return out;
        } else {
            vec3_.cross(tmpvec3, a, b);
            out[0] = tmpvec3[0];
            out[1] = tmpvec3[1];
            out[2] = tmpvec3[2];
            out[3] = 1 + dot;
            return quat_.normalize(out, out);
        }
    };
})();

/**
 * Sets the specified quaternion with values corresponding to the given
 * axes. Each axis is a vec3_ and is expected to be unit length and
 * perpendicular to all other specified axes.
 *
 * @param {vec3_} view  the vector representing the viewing direction
 * @param {vec3_} right the vector representing the local "right" direction
 * @param {vec3_} up    the vector representing the local "up" direction
 * @returns {quat_} out
 */
quat_.setAxes = (function() {
    var matr = mat3_.create();

    return function(out, view, right, up) {
        matr[0] = right[0];
        matr[3] = right[1];
        matr[6] = right[2];

        matr[1] = up[0];
        matr[4] = up[1];
        matr[7] = up[2];

        matr[2] = -view[0];
        matr[5] = -view[1];
        matr[8] = -view[2];

        return quat_.normalize(out, quat_.fromMat3(out, matr));
    };
})();

