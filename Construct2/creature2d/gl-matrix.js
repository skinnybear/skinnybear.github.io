/**
 * @class 2 Dimensional Vector
 * @name vec2_
 */

var vec2_ = {};

/**
 * Creates a new, empty vec2_
 *
 * @returns {vec2_} a new 2D vector
 */
vec2_.create = function() {
    var out = new GLMAT_ARRAY_TYPE(2);
    out[0] = 0;
    out[1] = 0;
    return out;
};

/**
 * Creates a new vec2_ initialized with values from an existing vector
 *
 * @param {vec2_} a vector to clone
 * @returns {vec2_} a new 2D vector
 */
vec2_.clone = function(a) {
    var out = new GLMAT_ARRAY_TYPE(2);
    out[0] = a[0];
    out[1] = a[1];
    return out;
};

/**
 * Creates a new vec2_ initialized with the given values
 *
 * @param {Number} x X component
 * @param {Number} y Y component
 * @returns {vec2_} a new 2D vector
 */
vec2_.fromValues = function(x, y) {
    var out = new GLMAT_ARRAY_TYPE(2);
    out[0] = x;
    out[1] = y;
    return out;
};

/**
 * Copy the values from one vec2_ to another
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a the source vector
 * @returns {vec2_} out
 */
vec2_.copy = function(out, a) {
    out[0] = a[0];
    out[1] = a[1];
    return out;
};

/**
 * Set the components of a vec2_ to the given values
 *
 * @param {vec2_} out the receiving vector
 * @param {Number} x X component
 * @param {Number} y Y component
 * @returns {vec2_} out
 */
vec2_.set = function(out, x, y) {
    out[0] = x;
    out[1] = y;
    return out;
};

/**
 * Adds two vec2_'s
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a the first operand
 * @param {vec2_} b the second operand
 * @returns {vec2_} out
 */
vec2_.add = function(out, a, b) {
    out[0] = a[0] + b[0];
    out[1] = a[1] + b[1];
    return out;
};

/**
 * Subtracts vector b from vector a
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a the first operand
 * @param {vec2_} b the second operand
 * @returns {vec2_} out
 */
vec2_.subtract = function(out, a, b) {
    out[0] = a[0] - b[0];
    out[1] = a[1] - b[1];
    return out;
};

/**
 * Alias for {@link vec2_.subtract}
 * @function
 */
vec2_.sub = vec2_.subtract;

/**
 * Multiplies two vec2_'s
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a the first operand
 * @param {vec2_} b the second operand
 * @returns {vec2_} out
 */
vec2_.multiply = function(out, a, b) {
    out[0] = a[0] * b[0];
    out[1] = a[1] * b[1];
    return out;
};

/**
 * Alias for {@link vec2_.multiply}
 * @function
 */
vec2_.mul = vec2_.multiply;

/**
 * Divides two vec2_'s
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a the first operand
 * @param {vec2_} b the second operand
 * @returns {vec2_} out
 */
vec2_.divide = function(out, a, b) {
    out[0] = a[0] / b[0];
    out[1] = a[1] / b[1];
    return out;
};

/**
 * Alias for {@link vec2_.divide}
 * @function
 */
vec2_.div = vec2_.divide;

/**
 * Returns the minimum of two vec2_'s
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a the first operand
 * @param {vec2_} b the second operand
 * @returns {vec2_} out
 */
vec2_.min = function(out, a, b) {
    out[0] = Math.min(a[0], b[0]);
    out[1] = Math.min(a[1], b[1]);
    return out;
};

/**
 * Returns the maximum of two vec2_'s
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a the first operand
 * @param {vec2_} b the second operand
 * @returns {vec2_} out
 */
vec2_.max = function(out, a, b) {
    out[0] = Math.max(a[0], b[0]);
    out[1] = Math.max(a[1], b[1]);
    return out;
};

/**
 * Scales a vec2_ by a scalar number
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a the vector to scale
 * @param {Number} b amount to scale the vector by
 * @returns {vec2_} out
 */
vec2_.scale = function(out, a, b) {
    out[0] = a[0] * b;
    out[1] = a[1] * b;
    return out;
};

/**
 * Adds two vec2_'s after scaling the second operand by a scalar value
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a the first operand
 * @param {vec2_} b the second operand
 * @param {Number} scale the amount to scale b by before adding
 * @returns {vec2_} out
 */
vec2_.scaleAndAdd = function(out, a, b, scale) {
    out[0] = a[0] + (b[0] * scale);
    out[1] = a[1] + (b[1] * scale);
    return out;
};

/**
 * Calculates the euclidian distance between two vec2_'s
 *
 * @param {vec2_} a the first operand
 * @param {vec2_} b the second operand
 * @returns {Number} distance between a and b
 */
vec2_.distance = function(a, b) {
    var x = b[0] - a[0],
        y = b[1] - a[1];
    return Math.sqrt(x*x + y*y);
};

/**
 * Alias for {@link vec2_.distance}
 * @function
 */
vec2_.dist = vec2_.distance;

/**
 * Calculates the squared euclidian distance between two vec2_'s
 *
 * @param {vec2_} a the first operand
 * @param {vec2_} b the second operand
 * @returns {Number} squared distance between a and b
 */
vec2_.squaredDistance = function(a, b) {
    var x = b[0] - a[0],
        y = b[1] - a[1];
    return x*x + y*y;
};

/**
 * Alias for {@link vec2_.squaredDistance}
 * @function
 */
vec2_.sqrDist = vec2_.squaredDistance;

/**
 * Calculates the length of a vec2_
 *
 * @param {vec2_} a vector to calculate length of
 * @returns {Number} length of a
 */
vec2_.length = function (a) {
    var x = a[0],
        y = a[1];
    return Math.sqrt(x*x + y*y);
};

/**
 * Alias for {@link vec2_.length}
 * @function
 */
vec2_.len = vec2_.length;

/**
 * Calculates the squared length of a vec2_
 *
 * @param {vec2_} a vector to calculate squared length of
 * @returns {Number} squared length of a
 */
vec2_.squaredLength = function (a) {
    var x = a[0],
        y = a[1];
    return x*x + y*y;
};

/**
 * Alias for {@link vec2_.squaredLength}
 * @function
 */
vec2_.sqrLen = vec2_.squaredLength;

/**
 * Negates the components of a vec2_
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a vector to negate
 * @returns {vec2_} out
 */
vec2_.negate = function(out, a) {
    out[0] = -a[0];
    out[1] = -a[1];
    return out;
};

/**
 * Returns the inverse of the components of a vec2_
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a vector to invert
 * @returns {vec2_} out
 */
vec2_.inverse = function(out, a) {
  out[0] = 1.0 / a[0];
  out[1] = 1.0 / a[1];
  return out;
};

/**
 * Normalize a vec2_
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a vector to normalize
 * @returns {vec2_} out
 */
vec2_.normalize = function(out, a) {
    var x = a[0],
        y = a[1];
    var len = x*x + y*y;
    if (len > 0) {
        //TODO: evaluate use of glm_invsqrt here?
        len = 1 / Math.sqrt(len);
        out[0] = a[0] * len;
        out[1] = a[1] * len;
    }
    return out;
};

/**
 * Calculates the dot product of two vec2_'s
 *
 * @param {vec2_} a the first operand
 * @param {vec2_} b the second operand
 * @returns {Number} dot product of a and b
 */
vec2_.dot = function (a, b) {
    return a[0] * b[0] + a[1] * b[1];
};

/**
 * Computes the cross product of two vec2_'s
 * Note that the cross product must by definition produce a 3D vector
 *
 * @param {vec3_} out the receiving vector
 * @param {vec2_} a the first operand
 * @param {vec2_} b the second operand
 * @returns {vec3_} out
 */
vec2_.cross = function(out, a, b) {
    var z = a[0] * b[1] - a[1] * b[0];
    out[0] = out[1] = 0;
    out[2] = z;
    return out;
};

/**
 * Performs a linear interpolation between two vec2_'s
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a the first operand
 * @param {vec2_} b the second operand
 * @param {Number} t interpolation amount between the two inputs
 * @returns {vec2_} out
 */
vec2_.lerp = function (out, a, b, t) {
    var ax = a[0],
        ay = a[1];
    out[0] = ax + t * (b[0] - ax);
    out[1] = ay + t * (b[1] - ay);
    return out;
};

/**
 * Generates a random vector with the given scale
 *
 * @param {vec2_} out the receiving vector
 * @param {Number} [scale] Length of the resulting vector. If ommitted, a unit vector will be returned
 * @returns {vec2_} out
 */
vec2_.random = function (out, scale) {
    scale = scale || 1.0;
    var r = GLMAT_RANDOM() * 2.0 * Math.PI;
    out[0] = Math.cos(r) * scale;
    out[1] = Math.sin(r) * scale;
    return out;
};

/**
 * Transforms the vec2_ with a mat2_
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a the vector to transform
 * @param {mat2_} m matrix to transform with
 * @returns {vec2_} out
 */
vec2_.transformMat2 = function(out, a, m) {
    var x = a[0],
        y = a[1];
    out[0] = m[0] * x + m[2] * y;
    out[1] = m[1] * x + m[3] * y;
    return out;
};

/**
 * Transforms the vec2_ with a mat2d_
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a the vector to transform
 * @param {mat2d_} m matrix to transform with
 * @returns {vec2_} out
 */
vec2_.transformMat2d = function(out, a, m) {
    var x = a[0],
        y = a[1];
    out[0] = m[0] * x + m[2] * y + m[4];
    out[1] = m[1] * x + m[3] * y + m[5];
    return out;
};

/**
 * Transforms the vec2_ with a mat3_
 * 3rd vector component is implicitly '1'
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a the vector to transform
 * @param {mat3_} m matrix to transform with
 * @returns {vec2_} out
 */
vec2_.transformMat3 = function(out, a, m) {
    var x = a[0],
        y = a[1];
    out[0] = m[0] * x + m[3] * y + m[6];
    out[1] = m[1] * x + m[4] * y + m[7];
    return out;
};

/**
 * Transforms the vec2_ with a mat4_
 * 3rd vector component is implicitly '0'
 * 4th vector component is implicitly '1'
 *
 * @param {vec2_} out the receiving vector
 * @param {vec2_} a the vector to transform
 * @param {mat4_} m matrix to transform with
 * @returns {vec2_} out
 */
vec2_.transformMat4 = function(out, a, m) {
    var x = a[0], 
        y = a[1];
    out[0] = m[0] * x + m[4] * y + m[12];
    out[1] = m[1] * x + m[5] * y + m[13];
    return out;
};

/**
 * Perform some operation over an array of vec2s.
 *
 * @param {Array} a the array of vectors to iterate over
 * @param {Number} stride Number of elements between the start of each vec2_. If 0 assumes tightly packed
 * @param {Number} offset Number of elements to skip at the beginning of the array
 * @param {Number} count Number of vec2s to iterate over. If 0 iterates over entire array
 * @param {Function} fn Function to call for each vector in the array
 * @param {Object} [arg] additional argument to pass to fn
 * @returns {Array} a
 * @function
 */
vec2_.forEach = (function() {
    var vec = vec2_.create();

    return function(a, stride, offset, count, fn, arg) {
        var i, l;
        if(!stride) {
            stride = 2;
        }

        if(!offset) {
            offset = 0;
        }
        
        if(count) {
            l = Math.min((count * stride) + offset, a.length);
        } else {
            l = a.length;
        }

        for(i = offset; i < l; i += stride) {
            vec[0] = a[i]; vec[1] = a[i+1];
            fn(vec, vec, arg);
            a[i] = vec[0]; a[i+1] = vec[1];
        }
        
        return a;
    };
})();

/**
 * Returns a string representation of a vector
 *
 * @param {vec2_} vec vector to represent as a string
 * @returns {String} string representation of the vector
 */
vec2_.str = function (a) {
    return 'vec2_(' + a[0] + ', ' + a[1] + ')';
};

