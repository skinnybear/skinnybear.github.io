mat2_.mul = mat2_.multiply;

/**
 * Rotates a mat2_ by the given angle
 *
 * @param {mat2_} out the receiving matrix
 * @param {mat2_} a the matrix to rotate
 * @param {Number} rad the angle to rotate the matrix by
 * @returns {mat2_} out
 */
mat2_.rotate = function (out, a, rad) {
    var a0 = a[0], a1 = a[1], a2 = a[2], a3 = a[3],
        s = Math.sin(rad),
        c = Math.cos(rad);
    out[0] = a0 *  c + a2 * s;
    out[1] = a1 *  c + a3 * s;
    out[2] = a0 * -s + a2 * c;
    out[3] = a1 * -s + a3 * c;
    return out;
};

/**
 * Scales the mat2_ by the dimensions in the given vec2_
 *
 * @param {mat2_} out the receiving matrix
 * @param {mat2_} a the matrix to rotate
 * @param {vec2_} v the vec2_ to scale the matrix by
 * @returns {mat2_} out
 **/
mat2_.scale = function(out, a, v) {
    var a0 = a[0], a1 = a[1], a2 = a[2], a3 = a[3],
        v0 = v[0], v1 = v[1];
    out[0] = a0 * v0;
    out[1] = a1 * v0;
    out[2] = a2 * v1;
    out[3] = a3 * v1;
    return out;
};

